# Deskripsi :   # Program akan memunculkan barisan angka dari 1 hingga N.

# KAMUS
# N = Integer

N = int(input("Masukan N : "))                     # Input, menentukan bilangan N yang akan dipilih sesuai kehendak pengguna.
for i in range(1, N+1) :                     # Fungsi pengulangan dari bilangan 1 hingga N.   
     print(i, end=" ")                     # Ouput, menampilkan semua bilangan i sampai N dengan jarak spasi                 